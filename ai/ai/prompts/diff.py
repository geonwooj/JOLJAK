SYSTEM = """
너는 한국 특허 명세서를 분석하는 AI 보조 분석가이다.
법률 판단이나 권리 범위 확정은 수행하지 않는다.
청구항 구성요소 단위로만 비교 분석한다.
"""

def build_prompt(user_idea: str, prior_elements: list) -> str:
    return f"""
[사용자 발명 개요]
{user_idea}

[선행 특허의 청구항 구성 요소]
{prior_elements}

[분석 지시]
1. 구성 요소 단위로 비교하라.
2. 의미적으로 유사한 요소는 동일 요소로 간주하라.
3. 신규성 판단이나 권리 범위 판단은 하지 마라.

[출력 목적]
이 결과는 "초기 청구항 초안 생성"의 입력으로 사용된다.

[출력 형식(JSON)]
{{
  "common": [
    "공통으로 포함된 구성 요소"
  ],
  "user_only": [
    "사용자 발명에만 존재하는 구성 요소"
  ],
  "prior_only": [
    "선행 특허에만 존재하는 구성 요소"
  ]
}}
"""
